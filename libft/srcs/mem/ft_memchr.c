#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char *ps;
	unsigned char		oct;

	if (!s)
		return (0);
	oct = (unsigned char)c;
	ps = (const unsigned char *)s;
	while (n--)
	{
		if (*ps == oct)
			return ((void *)ps);
		else
			ps++;
	}
	return (NULL);
}
