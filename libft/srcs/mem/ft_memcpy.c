#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*pdest;
	const unsigned char *psrc;

	pdest = dest;
	psrc = src;
	while (n--)
		pdest[n] = psrc[n];
	return (dest);
}
