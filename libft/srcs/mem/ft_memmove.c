#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	const char	*psrc;
	char		*pdest;

	psrc = (const char *)src;
	pdest = (char *)dest;
	if (psrc < pdest)
		while (n--)
			pdest[n] = psrc[n];
	else
		while (n--)
			*(pdest++) = *(psrc++);
	return (dest);
}
