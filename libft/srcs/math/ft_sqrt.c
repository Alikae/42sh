int		ft_sqrt(int nb)
{
	int demi;
	int i;

	i = 0;
	demi = nb / 2;
	if (nb < 1 || nb > 2147395600)
		return (0);
	if (nb == 1)
		return (1);
	while (++i < demi && i <= (nb / i))
		if ((i * i) == nb)
			return (i);
	return (0);
}
