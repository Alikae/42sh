#include "libft.h"

size_t		ft_strlen_w(const char *str)
{
	size_t len;
	size_t i;

	len = 0;
	i = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		if (!(str[i] & (1 << 7)) || (str[i] & (1 << 6)))
			len++;
		i++;
	}
	return (len);
}
