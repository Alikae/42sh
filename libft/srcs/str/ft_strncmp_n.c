#include "libft.h"

int		ft_strncmp_n(const char *s1, const char *s2, size_t n)
{
	unsigned char *ps1;
	unsigned char *ps2;

	ps1 = (unsigned char *)s1;
	ps2 = (unsigned char *)s2;
	if (!n)
		return (0);
	while (*ps1 == *ps2 && *ps1 && --n)
	{
		ps1++;
		ps2++;
	}
	return (n != 0 ? 1 : (int)(*ps1 - *ps2));
}
