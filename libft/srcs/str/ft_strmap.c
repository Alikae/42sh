#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	n;
	char	*new;

	n = 0;
	if (!s || !f)
		return (0);
	if (!(new = ft_strnew(ft_strlen(s))))
		return (NULL);
	while (s[n])
	{
		new[n] = f(s[n]);
		n++;
	}
	return (new);
}
