#include "libft.h"

char	*ft_strjoin_free(const char *s1, const char *s2, char *tofree)
{
	char *dest;

	dest = ft_strjoin(s1, s2);
	if (tofree)
		ft_memdel((void**)&tofree);
	return (dest);
}
