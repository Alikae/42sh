#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	size_t	n;

	n = 0;
	if (!s1 || !s2)
		return (!s1 && !s2 ? 1 : 0);
	while (s1[n] || s2[n])
	{
		if (s1[n] != s2[n])
			return (0);
		n++;
	}
	return (1);
}
