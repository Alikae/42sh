#include "libft.h"

char	*ft_str_dupsize(const char *s, size_t size)
{
	char *new;

	if (ft_strlen(s) > size || !(new = ft_strnew(size)))
		return (NULL);
	ft_strcpy(new, s);
	return (new);
}
