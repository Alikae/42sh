#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *s;

	if (!(s = (char*)malloc(sizeof(char) * size + 1)))
		return (NULL);
	s[size] = 0;
	while (size--)
	{
		s[size] = '\0';
	}
	return (s);
}
