#include "libft.h"
#include "sh_command_edition.h"

void	sh_strclean(char **str)
{
	int	i;

	i = 0;
	if (!str || !*str)
		return ;
	while (str[0][i])
	{
		if ((str[0][i] < 32 || str[0][i] == 127) && str[0][i] != '\n')
			str[0][i] = 32;
		i++;
	}
}
