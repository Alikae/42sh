#include "sh.h"
#include "sh_env.h"

t_env	*sh_getev(const char *key)
{
	t_env	*tmp;

	if (!(tmp = sh()->params))
		return (NULL);
	while (tmp && ft_strcmp(tmp->key, key))
		tmp = tmp->next;
	return (tmp);
}
