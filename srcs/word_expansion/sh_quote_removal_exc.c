int		sh_bq_exc(char c)
{
	if (c == '`' || c == '$' || c == '"' || c == '\\')
		return (1);
	return (0);
}

int		sh_inc_conditions(char c, int quote, int bquote)
{
	if ((c && c != '\'' && c != '"' && c != '\\') || quote != 0 || bquote)
		return (1);
	return (0);
}
