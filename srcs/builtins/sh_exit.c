#include "sh.h"

int				sh_exit(int ac, char **av, t_env **ev)
{
	(void)ac;
	(void)ev;
	sh()->abort_cmd = 1;
	sh()->exit = 1;
	if (av[0] && av[1])
		return (ft_atoi(av[1]));
	return (0);
}
